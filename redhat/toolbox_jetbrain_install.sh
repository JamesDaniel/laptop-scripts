# Install JetBrains Toolbox
wget https://download.jetbrains.com/toolbox/jetbrains-toolbox-1.3.2421.tar.gz
tar xzfv jetbrains-toolbox-1.3.2421.tar.gz
sudo mv jetbrains-toolbox-1.3.2421 /usr/share/jetbrains-toolbox-1.3.2421
rm jetbrains-toolbox-1.3.2421.tar.gz
echo 'To run Toolbox Execute the following commands as a user:'
echo 'cd /opt/jetbrains-toolbox-1.3.2421/'
echo './jetbrains-toolbox'
